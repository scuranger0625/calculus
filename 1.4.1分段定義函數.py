# 分段定義函數（Piecewise Function）是根據不同區間對自變數 x 給予不同的對應值。
# 它將自變數的定義域劃分為多個區間，並且每個區間有不同的函數表達式。
# 例如，根據 x 的值選擇不同的計算公式來求解對應的 y 值。
# 常見的形式為:
#     f(x) = { 
#                f1(x), 當 x 在區間 A1 上
#                f2(x), 當 x 在區間 A2 上
#                ... 
#                fn(x), 當 x 在區間 An 上
#             }


# 範例題目：
# f(x) = {
#            x^2 + 1  當 x ≤ 1
#            2x - 3   當 x > 1
#         }
# 求 f(-2), f(4), f(0), f(1)

# 定義分段函數 f(x)
def f(x):
    """
    分段定義函數 f(x):
    - 當 x <= 1 時，f(x) = x^2 + 1
    - 當 x > 1 時，f(x) = 2x - 3
    """
    if x <= 1:
        return x**2 + 1  # 當 x <= 1，使用 x^2 + 1
    else:
        return 2 * x - 3  # 當 x > 1，使用 2x - 3

# 求 f(-2), f(4), f(0), f(1) 的值
f_values = {
    "f(-2)": f(-2),
    "f(4)": f(4),
    "f(0)": f(0),
    "f(1)": f(1)
}

# 印出結果
for key, value in f_values.items():
    print(f"{key} = {value}")
